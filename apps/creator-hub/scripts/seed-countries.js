async function seedCountries() {
  console.log('Starting to seed countries...');
  
  let count = 0;
  const now = serverTimestamp();

  for (const country of countries) {
    try {
      const countryRef = doc(db, 'countries', country.id);
      
      const countryData = {
        ...country,
        createdAt: now,
        updatedAt: now,
      };

      await setDoc(countryRef, countryData);
      count++;
      console.log(`✅ Added ${country.name} (${country.code})`);
    } catch (error) {
      console.error(`❌ Error adding ${country.name}:`, error);
    }
  }

  console.log(`\n✅ Successfully seeded ${count} countries to Firestore!`);
  process.exit(0);
}

seedCountries().catch((error) => {
  console.error('❌ Error seeding countries:', error);
  process.exit(1);
});

